package alcohol.model;

public class HeartBean {
	private String mem_num;
	private String prod_num;
	private String heart_date;
	
	public String getMem_num() {
		return mem_num;
	}
	public void setMem_num(String mem_num) {
		this.mem_num = mem_num;
	}
	public String getProd_num() {
		return prod_num;
	}
	public void setProd_num(String prod_num) {
		this.prod_num = prod_num;
	}
	public String getHeart_date() {
		return heart_date;
	}
	public void setHeart_date(String heart_date) {
		this.heart_date = heart_date;
	}
	
	

}
